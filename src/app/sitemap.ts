import { MetadataRoute } from 'next';

export default function sitemap(): MetadataRoute.Sitemap {
  const baseUrl = 'https://bitotsav.bitmesra.ac.in';
  
  const routes = [
    '',
    '/events',
    '/schedule',
    '/sponsors',
    '/about',
    '/leaderboard',
    '/helpdesk',
  ].map((route) => ({
    url: `${baseUrl}${route}`,
    lastModified: new Date(),
    changeFrequency: 'daily' as const,
    priority: route === '' ? 1 : 0.8,
  }));

  return routes;
}
